/// <summary>
/// This enumerates the possible levels of accesibility.
/// </summary>
export enum AccessibilityLevels
{
    /// <summary>
    /// Anyone can access.
    /// </summary>
    Public,

    /// <summary>
    /// None.
    /// </summary>
    None,

    /// <summary>
    /// Inherited.
    /// </summary>
    Inherited,

    /// <summary>
    /// Only authentized people can access.
    /// </summary>
    Private,

    /// <summary>
    /// Only allowed people can access.
    /// </summary>
    Restricted,

    /// <summary>
    /// Any accessibility level.
    /// </summary>
    Any
}
