/// <summary>
/// This enumeration represents the possible data operators.
/// </summary>
export enum DataOperators
{
    /// <summary>
    /// None.
    /// </summary>
    None = "None",

    /// <summary>
    /// Undefined.
    /// </summary>
    Undefined = "Undefined",

    /// <summary>
    /// Exists.
    /// </summary>
    Exists = "Exists",

    /// <summary>
    /// Contains.
    /// </summary>
    Contains = "Contains",

    /// <summary>
    /// Has.
    /// </summary>
    Has = "Has",

    /// <summary>
    /// In.
    /// </summary>
    In = "In",

    /// <summary>
    /// Equal.
    /// </summary>
    EqualTo = "EqualTo",

    /// <summary>
    /// Different.
    /// </summary>
    DifferentFrom = "DifferentFrom",

    /// <summary>
    /// Lesser.
    /// </summary>
    LesserThan = "LesserThan",

    /// <summary>
    /// Lesser or equal.
    /// </summary>
    LesserOrEqualThan = "LesserOrEqualThan",

    /// <summary>
    /// Greater.
    /// </summary>
    GreaterThan = "GreaterThan",

    /// <summary>
    /// Greater or equal.
    /// </summary>
    GreaterOrEqualThan = "GreaterOrEqualThan",

    /// <summary>
    /// Any operator.
    /// </summary>
    Any = "Any"
}
