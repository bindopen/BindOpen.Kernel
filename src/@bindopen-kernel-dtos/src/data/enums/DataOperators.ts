/// <summary>
/// This enumeration represents the possible data operators.
/// </summary>
export enum DataOperators
{
    /// <summary>
    /// None.
    /// </summary>
    None,

    /// <summary>
    /// Undefined.
    /// </summary>
    Undefined,

    /// <summary>
    /// Exists.
    /// </summary>
    Exists,

    /// <summary>
    /// Contains.
    /// </summary>
    Contains,

    /// <summary>
    /// Has.
    /// </summary>
    Has,

    /// <summary>
    /// In.
    /// </summary>
    In,

    /// <summary>
    /// Equal.
    /// </summary>
    EqualTo,

    /// <summary>
    /// Different.
    /// </summary>
    DifferentFrom,

    /// <summary>
    /// Lesser.
    /// </summary>
    LesserThan,

    /// <summary>
    /// Lesser or equal.
    /// </summary>
    LesserOrEqualThan,

    /// <summary>
    /// Greater.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// Greater or equal.
    /// </summary>
    GreaterOrEqualThan,

    /// <summary>
    /// Any operator.
    /// </summary>
    Any
}
