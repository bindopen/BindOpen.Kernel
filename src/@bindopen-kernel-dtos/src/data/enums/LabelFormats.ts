/// <summary>
/// This enumeration lists all the possible label formats.
/// </summary>
export enum LabelFormats
{
    /// <summary>
    /// Any.
    /// </summary>
    Any,

    /// <summary>
    /// Only name.
    /// </summary>
    OnlyName,

    /// <summary>
    /// Name=value.
    /// </summary>
    NameEqualsValue,

    /// <summary>
    /// Name:value.
    /// </summary>
    NameColonValue,

    /// <summary>
    /// Name value.
    /// </summary>
    NameSpaceValue,

    /// <summary>
    /// Only value.
    /// </summary>
    OnlyValue,
}
