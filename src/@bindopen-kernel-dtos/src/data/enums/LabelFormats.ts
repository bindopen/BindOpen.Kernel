/// <summary>
/// This enumeration lists all the possible label formats.
/// </summary>
export enum LabelFormats
{
    /// <summary>
    /// Any.
    /// </summary>
    Any = "Any",

    /// <summary>
    /// Only name.
    /// </summary>
    OnlyName = "OnlyName",

    /// <summary>
    /// Name=value.
    /// </summary>
    NameEqualsValue = "NameEqualsValue",

    /// <summary>
    /// Name:value.
    /// </summary>
    NameColonValue = "NameColonValue",

    /// <summary>
    /// Name value.
    /// </summary>
    NameSpaceValue = "NameSpaceValue",

    /// <summary>
    /// Only value.
    /// </summary>
    OnlyValue = "OnlyValue"
}
