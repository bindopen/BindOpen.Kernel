/// <summary>
/// This enumeration represents the possible kinds for reference.
/// </summary>
export enum BdoReferenceKind
{
    /// <summary>
    /// Expression.
    /// </summary>
    Expression = "Expression",

    /// <summary>
    /// Target identifier.
    /// </summary>
    Identifier = "Identifier",

    /// <summary>
    /// Meta data.
    /// </summary>
    MetaData = "MetaData",

    /// <summary>
    /// None. Such as script word.
    /// </summary>
    None = "None",

    /// <summary>
    /// Any.
    /// </summary>
    Any = "Any"
}
