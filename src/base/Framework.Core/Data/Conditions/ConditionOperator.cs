namespace BindOpen.Framework.Core.Data.Conditions
{
    /// <summary>
    /// This enumeration lists all the possible operators of conditions.
    /// </summary>
    public enum ConditionOperator
    {
        /// <summary>
        /// Any.
        /// </summary>
        Any,

        /// <summary>
        /// Exist.
        /// </summary>
        Exist,

        /// <summary>
        /// EqualTo.
        /// </summary>
        EqualTo,

        /// <summary>
        /// LesserThan.
        /// </summary>
        LesserThan,

        /// <summary>
        /// GreaterThan.
        /// </summary>
        GreaterThan,

        /// <summary>
        /// DifferentFrom.
        /// </summary>
        DifferentFrom,
    }
}