/// <summary>
/// This enumeration represents the possible levels of requirement.
/// </summary>
export enum RequirementLevels
{
    /// <summary>
    /// None.
    /// </summary>
    None = "None",

    /// <summary>
    /// Undefined.
    /// </summary>
    Undefined = "Undefined",

    /// <summary>
    /// Forbidden.
    /// </summary>
    Forbidden = "Forbidden",

    /// <summary>
    /// Optional.
    /// </summary>
    Optional = "Optional",

    /// <summary>
    /// Required item.
    /// </summary>
    Required = "Required",

    /// <summary>
    /// Custom.
    /// </summary>
    Custom = "Custom",

    /// <summary>
    /// Any the requirement level.
    /// </summary>
    Any = "Any"
}
